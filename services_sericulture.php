<h4>Advantages of Sericulture</h4>
<p><b style="color:Green">Eco-friendly Activity:</b> -As a perennial crop with good foliage and root-spread, mulberry contributes to conservation and provides green cover.
<br>-Waste from silkworm rearing will be recycled as inputs to garden.
<br>-Dried mulberry twigs and branches are used as fuel in place of firewood and therefore reduce the pressure on vegetation/forest.
<br>-Developmental programmes initiated for mulberry plantation are mainly in upland areas where un-used cultivable land is made productive.
<br>-Currently, only concerning 0.1 % of the cultivable land in the country is beneath mulberry cultivation.

<br><br><b style="color:Green">Women friendly Occupation:</b>Women constitutes over hour of those employed in down-stream activities of sericulture in the country. This is the result of sericulture activities ranging from mulberry garden management, leaf harvesting and silkworm rearing is more effectively taken up by the women folks. Even silk reeling business together with weaving is largely supported by them. 
<br><br><b style="color:Green">Low Gestation, High Returns:</b> -Estimated investments of Rs.12,000 to 15,000 (excluding cost of land and rearing space) is adequate for undertaking mulberry cultivation and silkworm rearing in one acre of irrigated land.
<br> -Five crops can be taken in one year under tropical conditions.
<br> -It takes only six months for mulberry to grow for commencement of silkworm rearing. Once planted, it will go on supporting silkworm rearing year after year for 15-20 years depending on inputs and management provided.

</p>
