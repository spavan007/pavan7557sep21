<h4>Mulberry cultivation</h4>
<p>Sericulture is a prime commercial crop of Karnataka; it creates lots of employment and also a highly profitable crodiv. Naturally mulberry leaves is the only food of Silkworms, its growth depends on the supply of all the nutrients through this.
  <br><b style="color:Green">General Discription:</b>  – Mulberry is a fast growing deciduous woody perennial plant. It has a deep-root system. The leaves are simple, alternate, stipulate, petiolate, entire or lobed. Number of lobes varies from 1 to 5. Plants are generally dioecious. Inflorescence is catkin with pendent or drooping peduncle bearing unisexual flowers. Inflorescence is always auxiliary. <br>
  Male catkins are usually longer than the female catkins. Male flowers are loosely arranged and after shedding the pollen, the inflorescence dries and falls off. Number of parianth lobes are 4. Number of stamens are 4 and implexed in bud. Female inflorescence is usually short and the flowers are very compactly arranged. Number of parianth lobes are 4 and persistent. Ovary is one-celled and stigma is bifid. The chief pollinating agent in mulberry is wind. Fruit is a sorosis and the colour of the fruit is mainly violet black.
<br><br><b style="color:Green">Mulberry Leaf Production:</b>  Production and Quality of Mulberry leaf mainly depends on variety used, soil fertility, cultivation practices adopted, diseases and pest management practices adopted during the crop. Following this methods mulberry leaf production and quality can be improved year by year and made in to a sustainable crop.

<br><br><b style="color:Green">Climatic Requirement of Mulberry:</b> -Mulberry thrives under various climatic conditions ranging from temperate to tropic located north of equator between 28° N to 55°N latitude. The ideal range of temperature is from 24-28°C. It grows well in places with annual rainfall ranging from 600mm to 2500mm. In area with low rainfall, the growth is limited due to moisture stress resulting in low yields.
<br> On an average mulberry requires 85,000 gallons of water per hectare once every 10 days in case of loamy soils and 15 days in clayey soils. The atmospheric humidity in the range of 65-80% is ideally suited for mulberry growth.
<br><br><b style="color:Green">Soil Condition:</b> -Mulberry flourishes well in soils which are flat, deep, fertile, well drained, loamy to clayey, porous with good moisture holding capacity. The ideal range of soil pH is 6.2 to 6.8. The optimum pH required for mulberry is 6.5 to 6.8. Soil amendments may be used to correct the soil to get required pH.
</p>
</article>
