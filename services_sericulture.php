<h4>Establishment of mulberry under irrigted conditions</h4>
<p> <b style="color:Green">Selection of site:</b> -Mulberry flourishes well in soils that are flat, deep, fertile, well drained, loamy to clayey, and porous with a good moisture holding capacity. The ideal range of soil pH is 6.2 to 6.8. Mulberry can be grown in saline, alkaline and acidic soils after suitably amending the soils.
<br><b style="color:Green">Preparation of land:</b>-Land for mulberry cultivation is ploughed deep with heavy mould board plough up to a depth of 30-35 cm. Thereafter the land is repeatedly ploughed tw or three times with a country plough to bring the soil to a fine tilth. The land should be properly levelled. A basal dose of welldecomposed FYM or compost is applied at the rate of 20 tonnes/ha and thoroughly incorporated into the soil.
<br> <br><b style="color:Green">Planting:</b> -Plantations can be raised by using both cuttings and saplings. The varieties ideally suited for irrigated conditions are Kanva-2, S-36 and V-1. Branches of 6-9 months old and about 15 ml in diameter should be used for the preparation of cuttings of 15-18 cm in length with three to four healthy buds for raising in the nursery or for planting directly in the field.
<br><b style="color:Green">Spacing:</b> -Plant spacing of 90 x 90 cm is ideal for mulberry. Two cuttings per pit to be used for direct planting. When using saplings, only one sapling/pit is necessary. A paired row plantation with spacing of (90 + 150) cm x 60 cm, is recommended.
<br><br><b style="color:Green">Irrigation:</b> -The plantation should be taken up during the onset of the monsoon to take advantage of the rain. If the rain is not sufficient, the land should be irrigated at regular intervals of 8-14 days depending on the type of soil. About one and a half to two acre inches of water is required/irrigation.
<br><b style="color:Green">Fertilization:</b> -The total dose of fertilizer to be applied in the first year is 100 N: 50 P: 50 K/kg/ha/year. This is applied in two doses. The first dose is applied when the plantation is about two months old at the rate of 50 N: 50 P: 50 K/kg/ha. The second dose is applied after leaf harvesting at the rate of 50 kg N/ha.
</p>
