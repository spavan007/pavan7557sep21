<h4>Establishment of mulberry under rainfed conditions</h4>
<p> <b style="color:Green">Land Preparation:</b> -Land for mulberry cultivation is ploughed deep with a heavy mould board plough up to a depth of 30-35 cm. Thereafter the land is repeatedly ploughed two or three times with a country plough to bring the soil to a fine tilth. The land should be properly levelled. A basal dose of welldecomposed farmyard manure (FYM) or compost is applied at the rate of 10 tonnes/ha and thoroughly incorporated into the soil.
<br><b style="color:Green">Spacing:</b>-The spacing commonly followed for a rainfed garden is 90 x 90 cm Pits of 35 x 35 cm are prepared. About 1 kg FYM/pit should be added.
<br><br> <b style="color:Green"> Stakes and planting:</b>-Branches of 8-10 months old and about 50 mm in diameter should be used for the preparation of stakes of 22-25 cm length with five to six healthy buds. Three stakes are planted per pit in a triangular form with a spacing of 15 cm, leaving only one bud exposed above soil surface. If planting is done with saplings, then one sapling is sufficient perpit. Planting should be done during June/July after the onset of the monsoon.
<br> <b style="color:Green">Intercultivation:</b> -During the first year, intercultivation should be done manually. Once mulberry plants are established, bullock ploughing is carried out.
<br><br> <b style="color:Green">Fertilization:</b> -50N:25P:25K (kg/ha/year) in two doses. First dose: suphala (15:15:15) 167 kg, after 2 months of planting. Second dose: urea 55 kg or cam (100 kg) or ammonium sulfonate (125 kg), at end of September or early October before cessation of monsoon rains.


</p>
</article>
