<h4>Sericulture</h4>
<p> Karnataka is the homeland of Mysore Silk. Karnataka sericulture has a history of more than 215 years.It involves rearing of silkworms for the production of raw silk, which is the yarn obtained out of cocoons spun by certain species of insects.  Cultivation to feed the silkworms that spin silk cocoons and reeling the cocoons to unwind the silk filament for value added advantages like process and weaving are the major activities of sericulture. Silk has been blended with the life and culture of the Indians.
<br>India is the second largest producer of silk within the globe. Among the four kinds of silk created in 2015-16, Mulberry accounts for 71.8% (20,434 MT), Tasar 9.9% (2,818 MT), Eri 17.8% (5,054 MT) and Muga 0.6% (166 MT) of the total raw silk production of 28,472 MT.
<br><br> Mulberry sericulture is principally practised in 5 states i.e. Karnataka, Andhra Pradesh, Assam and Bodoland, West Bengal, Jharkhand and Tamil Nadu are major silk producing states in the country. North East has the distinctive distinction of being the sole region manufacturing four kinds of silk like Mulberry, Oak Tasar, Muga and Eri. Overall NE region contributes eighteen of India's total silk production.

</p>
</article>
