<br><br><b>Sugarcane: </b> – Sugarcane is an important cash crop of the State with an area of about 2.67 lakh hectares. The crop is sown or planted in three main seasons, July-August, October-November, and January-February. Sugarcane is the important commercial and industrial crop of Karnataka. Belagavi is the leading producer of sugarcane in Karnataka state and followed by Bagalkot, Mandya, Mysuru, Shivamogga, Davanagere, Koppal, Hassan, Vijayapura, Bidar, Ballari, and Haveri are important sugarcane-growing areas.

<br><br><b>Cotton: </b> – Cotton is an important cash crop of the state with an area of about 5.83 lakh hectares. Cotton is a fibre crop and it provides the raw material for cotton textiles. The main cotton-producing districts of Karnataka are Haveri, Dharwad, Gadag, Mysuru, Ballari, Raichur, Kalaburagi, Belagavi, Chitradurga, Davanagere, Koppal and Vijayapura. Among these, Haveri district ranks first in the production of cotton in the state, and Dharwad district ranks second.

<br><br><b>Tobacco: </b> – In the Karnataka tobacco farmers are worried over the proposal to increase tax on a cigarette and the
