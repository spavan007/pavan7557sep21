<h4>Organic farming</h4>
<p>Organic farming and Certification – The main objective of this programme is to promote the organic farming area by increasing the certified organic area and also to focus on the market-oriented commodity in potential areas to generate a bulk quantity of genuine organic produce through the farmer’s groups to meet the growing demand of domestic as well as an export market and ensure a continuous supply of required organic produce to the market. Then, this programme will be implemented through Karnataka State Organic Certification Agency (KSOCA) on project mode. There are more than 12,000 farmers in Karnataka who practice about 100% organic farming. Approximately 1 lakh farmers practice at least 50% organic farming.
<br><br> Organic farming aims at the production of quality and safe agricultural products which contain no chemical residues, following eco-friendly production methods and the farming systems that restore and maintain soil fertility.  The government of Karnataka has initiated action to promote organic farming  in the state and intends to formulate policies related to its promotion.</p>
