<h4>Primary crops </h4>
<p>The main crops grown are Rice, Ragi, Jowar (sorghum), maize, and pulses (Tur and gram) in addition to oilseeds and a number of other cash crops.Cashews, coconut, arecanut, cardamom, chillies, cotton, sugarcane and tobacco are also produced. Karnataka is the largest producer of coarse cereals,coffee,raw silk and tomatoes among the states in India. Horticultural crops are grown in an area of 16,300 km2 and the annual production is about 9.58 million tons.The income generated from horticulture constitutes over 40% of income generated from agriculture and it is about 17% of the state's GDP.In floriculture, Karnataka occupies the second position in India in terms of production and 700 tons of flowers (worth ₹500 million) were produced in 2004–05.


<br><br>A majority of the thirty-five billion rupee silk industry in India is headquartered in Karnataka State, primarily in Mysore and North Bangalore regions of Doddaballapura, the site of a planned 700 million "Silk City".</p>
</article>
