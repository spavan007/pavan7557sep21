<h4>Agriculture</h4>
<p>Agriculture is the primary occupation of most of Karnataka's rural people. A total of 123,100 km2 of land is cultivated in Karnataka, constituting 64.60% of the total geographical area of the state.
<br><br>According to the 2001 census, farmers and agricultural labourers formed 56% of the workforce of Karnataka.Agriculture in Karnataka is heavily dependent on the southwest monsoon since the extent of arid land in the state is second only to Rajasthan. Only 26.5% of sown area (30,900 km2) is subjected to irrigation.The state has three agricultural seasons – Kharif (April to September), Rabi (October to December) and Summer (January to March).</p>
</article>
